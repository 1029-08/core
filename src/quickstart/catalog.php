<h1>Customizing the look&amp;feel of the catalog</h1>
<p>Several components of LiteCommerce software can be used to customize appearance of your online store. 
They are: </p>
<ul>
	<li><a href="../admin.php?target=template_editor" target="right"><strong>Template editor</strong></a>, </li>
	<li><a href="../admin.php?target=css_edit" target="right"><strong>CSS editor</strong></a>, </li>
	<li><a href="../admin.php?target=image_edit" target="right"><strong>Image editor</strong></a>, and </li>
</ul>


<h2>Template editor</h2>
<p><a href="../admin.php?target=template_editor" target="right">Template editor</a> is a tool for customizing design templates of your store.
The templates are grouped into four tabs: </p>
<ul>
<li><a href="../admin.php?target=template_editor&editor=basic" target="right">Basic templates</a> group 
contains the 'Welcome page', Help menu pages ('Privacy statement', 'Terms&amp;Conditions' pages) and 
'Checkout success message' templates. </li>
<li><a href="../admin.php?target=template_editor&editor=mail" target="right">Mail templates</a> group
contains the full set of e-mail message templates.</li>
<li><a href="../admin.php?target=template_editor&editor=extra_pages" target="right">User-defined pages</a> group
lets you extend the front-end Help menu by adding custom design templates. </li>
<li><a href="../admin.php?target=template_editor&editor=advanced" target="right">Advanced templates</a> group 
provides the capability to modify all templates of the Customer and Administrator Zones and e-mail message templates.</li>
</ul>

<h2>CSS editor</h2>
<p><a href="../admin.php?target=css_edit" target="right">CSS editor</a> tool is indended for 
customizing the CSS style sheets of your store. It provides an interactive interface for setting 
custom colors, text fonts and background colors of various page elements.</p>

<h2>Image editor</h2>
<p><a href="../admin.php?target=image_edit" target="right">Image editor</a> tool is used 
to replace LiteCommerce standard graphic elements with your own graphics. It cannot be used to create/modify image files.</p>

