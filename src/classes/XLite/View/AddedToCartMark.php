/**
 * Added-to-cart mark widget
 *
 * @package XLite
 * @see     ____class_see____
 * @since   3.0
 */
class XLite_View_AddedToCartMark extends XLite_View_Abstract
{
    /**
     * Widget parameter names
     */

    const PARAM_PRODUCT = 'product';


    /**
     * Return widget default template
     *
     * @return string
     * @access protected
     * @since  3.0.0
     */
    protected function getDefaultTemplate()
    {
        return 'added_mark/body.tpl';
    }

    /**
     * Define widget parameters
     *
     * @return void
     * @access protected
     * @since  1.0.0
     */
    protected function defineWidgetParams()
    {
        parent::defineWidgetParams();

        $this->widgetParams += array(
            self::PARAM_PRODUCT => new XLite_Model_WidgetParam_Object('Product', null, false, 'XLite_Model_Product'),
        );
    }


    /**
     * Check widget visibility 
     * 
     * @return boolean
     * @access public
     * @since  3.0.0
     */
    public function isVisible()
    {
        $result = parent::isVisible();

        // FIXME
        if ($result) {
            $item = new XLite_Model_OrderItem();
            $item->set('product', $this->getParam(self::PARAM_PRODUCT));
            $result = $this->getCart()->isExistsItem($item);
        }

        return $result;
    }
}

