/**
 * Product sidebar box widget
 *
 * @package    View
 * @subpackage Widget
 * @since      3.0
 */
class XLite_View_ProductBox extends XLite_View_SideBarBox
{
    /**
     * Return title
     *
     * @return string
     * @access protected
     * @since  3.0.0 EE
     */
    protected function getHead()
    {
        return 'Product';
    }

    /**
     * Return templates directory name
     *
     * @return string
     * @access protected
     * @since  3.0.0 EE
     */
    protected function getDir()
    {
        return 'product_box';
    }

    /**
     * Get product 
     * 
     * @return XLite_Model_Product
     * @access protected
     * @see    ____func_see____
     * @since  3.0.0
     */
    protected function getProduct()
    {
        return $this->widgetParams['productId']->getObject($this->attributes['productId']);
    }

	/**
     * Define widget parameters
     *
     * @return void
     * @access protected
     * @since  1.0.0
     */
    protected function defineWidgetParams()
    {
		parent::defineWidgetParams();

        $this->widgetParams += array(
            'productId' => new XLite_Model_WidgetParam_ObjectId_Product('Product Id', 0),
        );
    }

    /**
     * Check if widget is visible
     *
     * @return bool
     * @access protected
     * @since  3.0.0 EE
     */
    public function isVisible()
    {
        return parent::isVisible() && $this->getProduct()->is('available');
    }
}

