<h1>Further configuration settings</h1>
<p>The further configuration steps consist of providing your company details, settings payment and shipping 
parameters and testing your store.</p>

<h2>1. Fill in your company information</h2>
<p>Go to the <a href="../admin.php?target=settings&page=Company" target="right"><strong>Settings / General settings / Company</strong></a> 
tab, fill in your company name, location and other details, and click on the 'Submit' button. The information you provide here will 
be displayed in your order invoices and notification e-mails. </p>

<h2>2. Setup payment methods</h2>
<p>Go to the <a href="../admin.php?target=payment_methods" target="right"><strong>Settings / Payment Methods</strong></a> section and 
review the list of available payment methods. Deactivate the methods you do not intend to use. </p>
<p><strong>Note:</strong> Default LiteCommerce installation does not support automatic credit card processing,
allowing only manual processing (using your offline credit card terminal). If you want to automatically process 
credit card payments online, you need a special add-on module which provides support for one of available online payment 
gateways, including Authorize.Net, 2Checkout.com, and PayPal. Corresponding online payment modules are
available at our <a href="https://secure.qtmsoft.com" target="_top">Customer HelpDesk site</a>.</p>

<h2>3. Setup shipping methods&amp;charges</h2>
<P>Go to the <a href="../admin.php?target=shipping_methods" target="right"><strong>Shipping settings / Methods</strong></a> tab 
and specify shipping methods your store is going to use. Your customers will be able to select their desired shipping 
methods from the list you define.</p>

<p>Next you need switch to the <a href="../admin.php?target=shipping_rates" target="right"><strong>Charges</strong></a> tab to specify 
shipping rates. When a customer selects a shipping method to use, corresponding shipping charge is added to the order total.</p>

<p><strong>Note:</strong> Optional LiteCommerce 'Real-time Shipping Quotes' package provides support for real-time shipping 
rate calculations. The package is available for sale at our 
<a href="https://secure.qtmsoft.com" target="_top">Customer HelpDesk site</a>.</p>

<h2>4. Test your settings</h2>
<p>Now that you have finished setting up your store, it is highly recommended that you test your settings by making some trial
purchases and carefully reviewing invoices, e-mail notifications and order details before announcing your site going live.
When using online payment methods, check if the test payment appears in your merchant transaction log.</p>
<p><strong>Hint:</strong> You should pay special attention to the configuration of add-on modules installed on your LiteCommerce 
shopping system.</p>

<h2>5. Congratulations!</h2>
<p>Now your store is all set and ready to go live.</p>
<p>Should you have any questions about customizing your store, please refer to 
<a href="http://www.litecommerce.com/manuals.html" target="_top">LiteCommerce Reference manual</a>
and/or contact our <a href="https://secure.qtmsoft.com" target="_top">HelpDesk</a>.</p>
<p>We wish you successful sales and many happy customers.</p>

